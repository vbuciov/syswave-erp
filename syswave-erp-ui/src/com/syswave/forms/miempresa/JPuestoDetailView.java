    //---------------------------------------------------------------------
    private void initEvents()
    {
        ActionListener actionListenerManager
                = new ActionListener()
                {
                    @Override
                    public void actionPerformed(ActionEvent evt)
                    {
                        finish_actionPerformed(evt);
                    }
                };
        
        jbAceptar.addActionListener(actionListenerManager);
        jbCancelar.addActionListener(actionListenerManager);
        
    }
    
    //---------------------------------------------------------------------
    private void finish_actionPerformed(ActionEvent evt)
    {
        Object sender = evt.getSource();
        
        if (sender == jbAceptar)
        {
            if (readElement(elementoActual))
                if (esNuevo)
                {
                    parent.onAcceptNewElement(elementoActual);
                    close();
                }
                else
                {
                    parent.onAcceptModifyElement(elementoActual);
                    close();
                };
        }
        else
            close();
    }

    //---------------------------------------------------------------------
    public void close()
    {
        setVisible(false);
        dispose();
    }
    
    //---------------------------------------------------------------------
    void prepareForNew()
    {
        this.setTitle("Nuevo puesto");
        elementoActual = new Puesto();
    }
    
    void prepareForModify(Puesto elemento)
    {
        this.elementoActual = elemento;
        esNuevo = false;
        this.setTitle("Modificando puesto " + elemento.getNombre().toString());
        writeElement(elemento);
    }
    
    //---------------------------------------------------------------------
    private void writeElement(Puesto elemento)
    {
        jtfNombre.setText(elemento.getNombre());
        jchActivo.setSelected(elemento.isActivo());
    }

    //---------------------------------------------------------------------
    private boolean readElement(Puesto elemento)
    {
        boolean resultado = false;
        
        if (!jtfNombre.getText().equals(""))
        {
            elemento.setNombre(jtfNombre.getText());
            elemento.setActivo(jchActivo.isSelected() ? true : false);
            resultado = true;
        }
        else
            JOptionPane.showMessageDialog(this, "Aseg√∫rese de proporcionar el nombre del puesto.", "", JOptionPane.PLAIN_MESSAGE);
        
        return resultado;
    }
