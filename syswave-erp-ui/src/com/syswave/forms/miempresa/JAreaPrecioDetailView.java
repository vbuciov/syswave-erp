   private void initEvents()
   {
      ActionListener actionListenerManager
                     = new ActionListener()
      {
         @Override
         public void actionPerformed(ActionEvent evt)
         {
            finish_actionPerformed(evt);
         }
      };
      
      
      jbAceptar.addActionListener(actionListenerManager);
      jbCancelar.addActionListener(actionListenerManager);
   }

   
   //---------------------------------------------------------------------
   private void finish_actionPerformed (ActionEvent evt)
   {
      Object sender = evt.getSource();
      
      if (sender == jbAceptar)
      {
         if (readElement(elementoActual))
         {
            if (esNuevo)
               parent.onAcceptNewElement(elementoActual);

            else 
            {
               elementoActual.setModified();
               parent.onAcceptModifyElement(elementoActual);
            }
            
            close();
         }
      }
      
      else
         close ();
   }
   
   //---------------------------------------------------------------------
   public void prepareForNew ()
   {
      elementoActual = new AreaPrecio();
      esNuevo = true;
      bsTipoPrecioRender.setData(parent.obtenerTipoPrecios());
      this.setTitle("Nueva localidad");
   }
   
   //---------------------------------------------------------------------
   public void prepareForModify (AreaPrecio elemento)
   {
      this.elementoActual = elemento;
      esNuevo = false;
      bsTipoPrecioRender.setData(parent.obtenerTipoPrecios());
      this.setTitle(String.format("Modificando %s", elemento.getDescripcion()));
      writeElement(elemento);
   }
   
   //---------------------------------------------------------------------
   public void writeElement (AreaPrecio elemento)
   {
      jtfNombre.setText(elemento.getDescripcion());
      jcbTIpoPrecio.setSelectedItem(bsTipoPrecioRender.getElementAt(bsTipoPrecioRender.indexOfValue(elemento.getEsTipo())));
      jchkCostoDirecto.setSelected(elemento.esCostoInDirecto());
      jchkCostoVariable.setSelected(elemento.esCostoVariable());
   }
   
   //---------------------------------------------------------------------
   private boolean readElement (AreaPrecio elemento)
   {
      boolean resultado= false;
      String mensaje = "";
      
      if (!jtfNombre.getText().isEmpty() /*&& !jtfSiglas.getText().isEmpty()*/)
      {
         resultado = true;
         elemento.setDescripcion(jtfNombre.getText());
         elemento.setEsTipo((int) bsTipoPrecioRender.getSelectedValue());
         elemento.setCostoDirecto(!jchkCostoDirecto.isSelected());
         elemento.setEsCostoVariable(jchkCostoVariable.isSelected());
         if (!elemento.isSet())
            elemento.setModified();
      }
      
      else
         mensaje = "Asegurese de proporcionar el nombre";
      
      if (!resultado)
         JOptionPane.showMessageDialog(this, mensaje, "", JOptionPane.PLAIN_MESSAGE);
      
      return resultado;
   }
  
    //---------------------------------------------------------------------
   public void close ()
   {
      this.setVisible(false);
      this.dispose();
   }


